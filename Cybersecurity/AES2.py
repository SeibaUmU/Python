def ShiftRows(state):
    return state[:4] + state[5:8] + [state[4]] + state[10:12] + state[8:10] + [state[15]] + state[12:15]
def InvShiftRow(state):
    return state[:4] +  [state[7]] + state[4:7] + state[10:12] + state[8:10] + state[13:16] + [state[12]]
def MixColumns(state):
    # Chuyển đổi state từ danh sách 1 chiều thành ma trận 4x4
    state_2d = [state[i:i+4] for i in range(0, len(state), 4)]
    # Áp dụng phép toán trên ma trận 4x4
    for i in range(4):
        a = [state_2d[j][i] for j in range(4)]
        state_2d[0][i] = GF_mult(0x02, a[0]) ^ GF_mult(0x03, a[1]) ^ a[2] ^ a[3]
        state_2d[1][i] = a[0] ^ GF_mult(0x02, a[1]) ^ GF_mult(0x03, a[2]) ^ a[3]
        state_2d[2][i] = a[0] ^ a[1] ^ GF_mult(0x02, a[2]) ^ GF_mult(0x03, a[3])
        state_2d[3][i] = GF_mult(0x03, a[0]) ^ a[1] ^ a[2] ^ GF_mult(0x02, a[3])
    # Chuyển đổi state_2d trở lại danh sách 1 chiều
    state = [elem for row in state_2d for elem in row]
    return state


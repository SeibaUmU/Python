def AES_encrypt_block(block, key):
    state = list(block)
    roundKeys = KeyExpansion(key)
    state = AddRoundKey(state, roundKeys[0])
    #9 vong lap
    for round in range(1,10):
        state = subBytes(state)
        state = ShiftRows(state)
        state = MixColumns(state)
        state = AddRoundKey(state, roundKeys[round])
    #vong lap 10
    state = subBytes(state)
    state = ShiftRows(state)
    state = AddRoundKey(state, roundKeys[10])
    return state
def AES_decrypt_block(cipherblock, key):
    state = list(cipherblock)
    roundKeys = KeyExpansion(key)
    state = AddRoundKey(state, roundKeys[10])
    #9 vong lap
    for round in range(9, 0, -1):
        state = InvShiftRow(state)
        state = InvSubBytes(state)
        state = AddRoundKey(state, roundKeys[round])
        state = InvMixColumns(state)
    #vong lap 0
    state = InvShiftRow(state)
    state = InvSubBytes(state)
    state = AddRoundKey(state, roundKeys[0])
    return state

def AES_encrypt(plaintext, key):
    key = hashkey16byte(key)
    plaintext = pad(plaintext.encode('utf-8'))
    ciphertext = []
    for i in range(0, len(plaintext), 16):
        block = plaintext[i:i + 16]
        encrypt_block = AES_encrypt_block(block, key)
        ciphertext.extend(encrypt_block)
    return ciphertext
def AES_decrypt(ciphertext, key):
    key = hashkey16byte(key)
    plain_text = []
    for i in range(0, len(ciphertext), 16):
        block = ciphertext[i:i+16]
        decrypted_block = AES_decrypt_block(block, key)
        plain_text.extend(decrypted_block)
    return unpad(bytes(plain_text)).decode('utf-8') 
